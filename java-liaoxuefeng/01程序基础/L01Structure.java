/**
 * 文档注释
 * 
 * 类名：字母开头，字母+数字+下划线
 * 
 * 类前加public，可以从命令行执行。不加则无法从命令行执行。
 * 
 * @author lwabish
 * @since 2020
 */
public class L01Structure {
    /**
     * 方法命名可用字符和类一样
     * 
     * @param args
     */
    public static void main(String[] args) {
        // 单行注释
        System.out.println("Hello,World");
        /**
         * 多行注释
         * 
         * 缩进不强制，分号强制
         */
    }
}
